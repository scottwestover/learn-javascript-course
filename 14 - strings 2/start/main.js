// Strings
var text1 = 'hi';
var text2 = "hi";

// escaping: \' \" \\
var text3 = 'hi \\ there, I\'m looking for someone';
console.log(text3);

// breaking up long strings
var text4 = 'hi there, how' +
  ' \n are you?';
console.log(text4);

// strings as Objects: using `new String()` - not recommended to use this
var text5 = new String('hi');
console.log(text5);

